-- Уникальность по имени поставщика — нужна сидеру каталога, чтобы при
-- параллельном запуске двух процессов плейсхолдер «Не указан» гарантированно
-- не задублировался. До этой миграции дедупликация была только через
-- INSERT ... WHERE NOT EXISTS, что без UNIQUE не даёт жёстких гарантий
-- при гонках (см. seed-catalog.ts).
CREATE UNIQUE INDEX IF NOT EXISTS supplier_name_unique ON supplier (name);
