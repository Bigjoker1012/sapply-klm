/**
 * Регрессионные тесты на парсер КД «Ведомость по партиям» (Липковская/1С).
 *
 * Защищают от известной регрессии: чтение наименования и количества из НЕВЕРНЫХ
 * колонок. В выгрузке 1С наименование — в колонке A (index 0), а «Конечный
 * остаток (количество)» — в колонке H (index 7); index 8 — это стоимость.
 * Раньше парсер брал имя из index 1 (число) и кол-во из index 8 (стоимость),
 * из-за чего распознавалось всего несколько позиций.
 *
 * Числа в выгрузке — US-формата (1,500.000): запятая = разделитель тысяч,
 * точка = десятичная. Парсер должен их корректно читать.
 *
 * Запуск: `npm test`
 */
import test from "node:test";
import assert from "node:assert/strict";
import * as XLSX from "xlsx";
import { parseKdExcel } from "./excelParser";

/** Строит .xls-буфер из матрицы строк (как лист TDSheet в выгрузке 1С). */
function buildKdBuffer(rows: any[][]): Buffer {
  const ws = XLSX.utils.aoa_to_sheet(rows);
  const wb = XLSX.utils.book_new();
  XLSX.utils.book_append_sheet(wb, ws, "TDSheet");
  return XLSX.write(wb, { type: "buffer", bookType: "xls" });
}

// Колонки: A=Номенклатура, B=Нач.ост.Кол-во, C=Нач.ост.Стоим., D=Приход Кол-во,
// E=Приход Стоим., F=Расход Кол-во, G=Расход Стоим., H=Конечный остаток Кол-во,
// I=Конечный остаток Стоимость.
const KD_ROWS: any[][] = [
  ["Ведомость по партиям товаров на складах (регл)", "", "", "", "", "", "", "", ""],
  ["Период: 18 мая 2026 г.", "", "", "", "", "", "", "", ""],
  ["Показатели: ...", "", "", "", "", "", "", "", ""],
  ["Склад", "Начальный остаток", "", "Приход", "", "Расход", "", "Конечный остаток", ""],
  ["Номенклатура", "Количество", "Стоимость", "Количество", "Стоимость", "Количество", "Стоимость", "Количество", "Стоимость"],
  ["Склад КОРМОВЫЕ ДОБАВКИ  (Липковская)", 97545.494, 1110544.17, 3199, 102496.07, 3200.5, 112157.8, 97543.994, 1100882.44],
  ["Актиген мешок 25 кг. партия IE002856", 1500, 33086.22, 200, 4411.5, 200, 4411.5, 1500, 33086.22],
  ["Биоплекс Марганец (мешки 25 кг), партия 25791", 2500, 81337.41, "", "", "", "", 2500, 81337.41],
  // Позиция с нулевым конечным остатком — должна быть отброшена.
  ["КК Альсэдум -К/ПХЧ-25*", 1, 6605.8, "", "", 1, 6605.8, 0, 0],
  // Позиция без суффикса партии — baseName = всё имя, batchCode пустой.
  ["Звено соединительное 120НК DID", 1, 63.86, "", "", "", "", 1, 63.86],
];

test("parseKdExcel: имя из колонки A, кол-во из колонки H (не стоимость)", () => {
  const rows = parseKdExcel(buildKdBuffer(KD_ROWS));
  const aktigen = rows.find(r => r.baseName.startsWith("Актиген"));
  assert.ok(aktigen, "Актиген должен распознаться");
  // qty = конечный остаток КОЛИЧЕСТВО (1500), а не стоимость (33086.22).
  assert.equal(aktigen!.qty, 1500);
  assert.equal(aktigen!.batchCode, "IE002856");
  assert.equal(aktigen!.baseName, "Актиген мешок 25 кг.");
});

test("parseKdExcel: пропускает шапку и строку-итог склада", () => {
  const rows = parseKdExcel(buildKdBuffer(KD_ROWS));
  assert.ok(!rows.some(r => /^склад/i.test(r.vendorName)), "строка склада-итога не должна попасть");
  assert.ok(!rows.some(r => /^ведомость|^номенклатура|^период|^показат/i.test(r.vendorName)), "шапка не должна попасть");
});

test("parseKdExcel: отбрасывает позиции с нулевым конечным остатком", () => {
  const rows = parseKdExcel(buildKdBuffer(KD_ROWS));
  assert.ok(!rows.some(r => r.baseName.startsWith("КК Альсэдум")), "нулевой остаток отбрасывается");
});

test("parseKdExcel: позиция без суффикса партии — batchCode пустой", () => {
  const rows = parseKdExcel(buildKdBuffer(KD_ROWS));
  const zveno = rows.find(r => r.baseName.startsWith("Звено"));
  assert.ok(zveno, "позиция без партии должна распознаться");
  assert.equal(zveno!.batchCode, "");
  assert.equal(zveno!.qty, 1);
});

test("parseKdExcel: пропускает чисто числовые ячейки в колонке наименования", () => {
  // В выгрузке встречаются строки, где в колонке A — число (сумма/количество),
  // а не наименование. Они не должны попадать в распознавание как «сырьё».
  const rows = parseKdExcel(buildKdBuffer([
    ["Номенклатура", "Количество", "Стоимость", "", "", "", "", "Количество", "Стоимость"],
    ["1500", 1, 0, "", "", "", "", 1500, 0],
    ["6 799", 1, 0, "", "", "", "", 425, 0],
    ["1,500.000", 1, 0, "", "", "", "", 2500, 0],
    ["Реальное сырьё партия Z-9", 1, 0, "", "", "", "", 10, 0],
  ]));
  assert.ok(!rows.some(r => /^[\d\s.,]+$/.test(r.baseName)), "числовые имена должны быть отброшены");
  assert.ok(rows.some(r => r.baseName.startsWith("Реальное")), "реальное сырьё должно остаться");
});

test("parseKdExcel: US-формат числа со строковой запятой-тысячником (1,500.000)", () => {
  // Если ячейка пришла как строка с US-разделителями, запятые удаляются.
  const rows = parseKdExcel(buildKdBuffer([
    ["Номенклатура", "Количество", "Стоимость", "", "", "", "", "Количество", "Стоимость"],
    ["Тестовое сырьё партия X-1", "10.000", "0", "", "", "", "", "1,500.000", "0"],
  ]));
  const item = rows.find(r => r.baseName.startsWith("Тестовое"));
  assert.ok(item, "позиция должна распознаться");
  assert.equal(item!.qty, 1500);
});

// ─── Новый формат «по партиям» (7 колонок, конечный остаток в col G / idx 6) ──
// A=Номенклатура, B=Дата поставки, C=Срок годности, D=Нач.ост., E=Приход,
// F=Расход, G=Конечный остаток. Под номенклатурой идут строки серий (с датами
// в B/C) и строки документов движения (дублирование «X, X»), которые НЕ являются
// сырьём и должны отбрасываться. Колонка кол-ва ищется динамически по заголовку.
const KD_NESTED_ROWS: any[][] = [
  ["Ведомость по партиям товаров на складах (регл)", "", "", "", "", "", ""],
  ["Показатели: Начальный остаток(Количество); ...Конечный остаток(Количество);", "", "", "", "", "", ""],
  ["Склад", "", "", "Начальный остаток", "Приход", "Расход", "Конечный остаток"],
  ["Серия номенклатуры", "Дата поставки", "Срок годности", "Количество", "Количество", "Количество", "Количество"],
  ["Склад КОРМОВЫЕ ДОБАВКИ  (Липковская)", "", "", "85,034.325", "", "3,800.000", "81,234.325"],
  ["Актиген мешок 25 кг. партия IE002856", "", "", "675.000", "", "", "675.000"],
  ["621 от 05.05.26", "05.05.26", "19.02.29", "675.000", "", "", "675.000"],
  ["Перемещение товаров К0000000889, Перемещение товаров К0000000889", "", "", "675.000", "", "", "675.000"],
  [", ", "", "", "675.000", "", "", "675.000"],
];

test("parseKdExcel: новый формат — кол-во из col G (idx 6), а не fallback col H", () => {
  const rows = parseKdExcel(buildKdBuffer(KD_NESTED_ROWS));
  const aktigen = rows.find(r => r.baseName.startsWith("Актиген"));
  assert.ok(aktigen, "Актиген должен распознаться в новом формате");
  assert.equal(aktigen!.qty, 675);
  assert.equal(aktigen!.batchCode, "IE002856");
});

test("parseKdExcel: новый формат — отбрасывает строки серий и документов движения", () => {
  const rows = parseKdExcel(buildKdBuffer(KD_NESTED_ROWS));
  // Распознаётся только номенклатура (Актиген), без серий и документов.
  assert.equal(rows.length, 1);
  assert.ok(!rows.some(r => /^\d{1,3} от /.test(r.vendorName)), "строка серии (621 от ...) не должна попасть");
  assert.ok(!rows.some(r => /^Перемещение/.test(r.vendorName)), "строка документа движения не должна попасть");
});

test("parseKdExcel: заголовок «Конечный остаток (количество)» тоже распознаётся", () => {
  const rows = parseKdExcel(buildKdBuffer([
    ["Номенклатура", "", "", "Начальный остаток", "Приход", "Расход", "Конечный остаток (количество)"],
    ["Сырьё с длинным заголовком партия Q-7", "", "", "100.000", "", "", "42.000"],
  ]));
  const item = rows.find(r => r.baseName.startsWith("Сырьё"));
  assert.ok(item, "позиция должна распознаться при расширенном заголовке");
  assert.equal(item!.qty, 42);
});
